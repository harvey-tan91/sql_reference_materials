/*
SQL Commands are divided into 4 categories

1) DDL – Data Definition Language
DDL are used to define the database schema and the structure of the database and tables.
List of DDL commands includes
- CREATE
- DROP
- ALTER
- TRUNCATE

2) DQL – Data Query Language
DQL are used to perform queries on the database.
List of DQL commands includes
- SELECT

3) DML – Data Manipulation Language
DML are used to manipulate with the data in the database.
List of DML commands includes
- INSERT
- UPDATE
- DELETE
- LOCK

4) DCL – Data Control Language
DCL are used to control the rights, access and permissions of the database system
List of DCL commands includes
- GRANT
- REVOKE 

*/


